def is_cancer_indication(indication):
    """判断是否为抗肿瘤药物"""
    if not indication:
        return False
    
    keywords = [
        '癌', '肿瘤', '白血病', '淋巴瘤', '肉瘤', '骨髓瘤', '瘤',
        '肺癌', 'NSCLC', 'SCLC', '非小细胞', '小细胞',
        '乳腺癌', '肝癌', '胃癌', '结直肠', '胰腺', '食管', '胆管',
        '肾癌', '膀胱', '前列腺', '睾丸',
        '卵巢', '宫颈', '子宫', '子宫内膜',
        '黑色素', '脑', '胶质', '头颈', '鼻咽', '甲状腺',
        'B细胞', 'MM', 'CLL', 'CML', 'MDS',
        'EGFR', 'ALK', 'ROS1', 'RET', 'MET', 'HER2', 'PD-1', 'PD-L1',
        'BCMA', 'CD38', 'CD20', 'VEGFR', 'FGFR', 'NTRK', 'KRAS', 'BRAF',
        '单抗', '抗体', 'ADC', '免疫治疗',
        '腺癌', '鳞癌', '神经内分泌', '实体瘤', '转移性', '晚期',
        '间皮瘤', '皮肤癌', '骨肉瘤', '尤文肉瘤', '脂肪肉瘤',
        '胃肠道间质瘤', 'GIST', '壶腹癌', '十二指肠癌', '尿路上皮癌'
    ]
    
    for keyword in keywords:
        if keyword.lower() in indication.lower():
            return True
    
    return False

def save_to_db():
    """保存到数据库"""
    db_path = os.path.join('data', 'medical_info.db')
    db = init_database(db_path)
    
    all_drugs = build_complete_data()
    now = datetime.now().strftime('%Y-%m-%d')
    
    # 清空表
    conn = db.connect()
    cursor = conn.cursor()
    cursor.execute("DELETE FROM cde_special_drugs")
    conn.commit()
    
    count = 0
    cancer_drug_count = 0
    
    for i, drug in enumerate(all_drugs):
        # 判断是否抗肿瘤
        is_cancer = drug.get('is_cancer', True) if 'is_cancer' in drug else is_cancer_indication(drug['拟定适应症'])
        
        if not is_cancer:
            continue
        
        cancer_drug_count += 1
        
        record = {
            'cde_id': f"CDE-{drug['名单类型'][:2]}-{drug['序号'] if drug['序号'] else str(i+1)}",
            'drug_name': drug['药物名称'],
            'drug_type': drug['名单类型'],
            'indication': drug['拟定适应症'],
            'applicant': drug['申请人'],
            'application_date': drug['申请日期'],
            'acceptance_number': drug['受理号'],
            'approval_date': '',
            'status': '已纳入',
            'priority_type': drug['名单类型'] if drug['名单类型'] == '优先审评' else '',
            'breakthrough_type': drug['名单类型'] if drug['名单类型'] == '突破性治疗' else '',
            'trial_info': '',
            'molecular_target': '',
            'gene_marker': '',
            'reference_drug': '',
            'description': '',
            'detail_url': 'https://www.cde.org.cn/main/xxgk/listpage/b40868b5e21c038a6aa8b4319d21b07d',
            'created_at': now,
            'updated_at': now
        }
        
        try:
            db.execute_insert('cde_special_drugs', record)
            count += 1
        except Exception as e:
            print(f"插入失败: {drug['药物名称']} - {e}")
    
    print("\n" + "="*70)
    print("✅ CDE特殊品种数据库重建完成！")
    print("="*70)
    print(f"\n成功插入 {count} 条抗肿瘤药物记录")
    
    # 统计
    results = db.execute_query('''
        SELECT drug_type, COUNT(*) as cnt 
        FROM cde_special_drugs 
        GROUP BY drug_type
    ''')
    
    print("\n数据库统计:")
    for r in results:
        print(f"  {r['drug_type']}: {r['cnt']} 条")
    
    # 显示一些药物
    print("\n最近添加的药物示例:")
    sample = db.execute_query('''
        SELECT drug_name, drug_type, acceptance_number, applicant, indication 
        FROM cde_special_drugs 
        ORDER BY drug_type DESC, updated_at DESC 
        LIMIT 15
    ''')
    
    for s in sample:
        print(f"\n  [{s['drug_type']}] {s['drug_name']}")
        print(f"    受理号: {s['acceptance_number']}")
        print(f"    申请人: {s['applicant']}")
        print(f"    适应症: {s['indication'][:70]}..." if len(s['indication'])>70 else f"    适应症: {s['indication']}")
    
    print("\n" + "="*70)

if __name__ == '__main__':
    save_to_db()
